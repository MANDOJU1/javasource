package exam;

public class Printer {

  void println(int number) {
    System.out.println(number);
  }

  void println(boolean flag) {
    System.out.println(flag);
  }

  void println(double number) {
    System.out.println(number);
  }

  void println(String name) {
    System.out.println(name);
  }
}
